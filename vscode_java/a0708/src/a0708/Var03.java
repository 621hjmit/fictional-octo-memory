package a0708;

public class Var03 {
    public static void main(String[] args) {
        int var1 = 0b1011; // 0b ->2진수
        int var2 = 0206;   // 0으로 시작하면 8진수
        int var3 = 365; //10진수
        int var4 = 0xb3; //0x  헥사 

        System.out.println("var1: " + var1);
        System.out.println("var2: " + var2);
        System.out.println("var3: " + var3);
        System.out.println("var4: " + var4);
    }
}
